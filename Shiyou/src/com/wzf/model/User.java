package com.wzf.model;

public class User {
	private String id;
	private String username;
	private String privlg;
	private String json_type;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPrivlg() {
		return privlg;
	}
	public void setPrivlg(String privlg) {
		this.privlg = privlg;
	}
	public String getJson_type() {
		return json_type;
	}
	public void setJson_type(String jsonType) {
		json_type = jsonType;
	}
	

}
