package com.wzf.model;

public class Clas {
	private int id;
	private String period;
	private String group;
	private String time_beg;
	private String time_end;
	private String class_addr;
	private String residue;
	private String maxpeople;
	private String class_teacher;
	private String tel_teacher;
	private String status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public String getTime_beg() {
		return time_beg;
	}
	public void setTime_beg(String timeBeg) {
		time_beg = timeBeg;
	}
	public String getTime_end() {
		return time_end;
	}
	public void setTime_end(String timeEnd) {
		time_end = timeEnd;
	}
	public String getClass_addr() {
		return class_addr;
	}
	public void setClass_addr(String classAddr) {
		class_addr = classAddr;
	}
	public String getResidue() {
		return residue;
	}
	public void setResidue(String residue) {
		this.residue = residue;
	}
	public String getMaxpeople() {
		return maxpeople;
	}
	public void setMaxpeople(String maxpeople) {
		this.maxpeople = maxpeople;
	}
	public String getClass_teacher() {
		return class_teacher;
	}
	public void setClass_teacher(String classTeacher) {
		class_teacher = classTeacher;
	}
	public String getTel_teacher() {
		return tel_teacher;
	}
	public void setTel_teacher(String telTeacher) {
		tel_teacher = telTeacher;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
