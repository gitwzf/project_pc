package com.wzf.model;

public class Groups {
	public String[] id;
	public String[] name;
	public String[] cnt;
	public String[] getId() {
		return id;
	}
	public void setId(String[] id) {
		this.id = id;
	}
	public String[] getName() {
		return name;
	}
	public void setName(String[] name) {
		this.name = name;
	}
	public String[] getCnt() {
		return cnt;
	}
	public void setCnt(String[] cnt) {
		this.cnt = cnt;
	}
	

}
