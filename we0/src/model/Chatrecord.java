package model;

public class Chatrecord {
	public String userid;
	public String usersay;
	public String wreply;
	public String wreply_type;
	public String time;
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUsersay() {
		return usersay;
	}
	public void setUsersay(String usersay) {
		this.usersay = usersay;
	}
	public String getWreply() {
		return wreply;
	}
	public void setWreply(String wreply) {
		this.wreply = wreply;
	}
	public String getWreply_type() {
		return wreply_type;
	}
	public void setWreply_type(String wreplyType) {
		wreply_type = wreplyType;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
}
