package com.wzf.model;

public class Page {
	private String p;
	private String maxp;
	
	public Page(String p, String maxp) {
		this.p = p;
		this.maxp = maxp;
	}
	public String getP() {
		return p;
	}
	public void setP(String p) {
		this.p = p;
	}
	public String getMaxp() {
		return maxp;
	}
	public void setMaxp(String maxp) {
		this.maxp = maxp;
	}
	
}
