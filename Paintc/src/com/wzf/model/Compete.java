package com.wzf.model;

public class Compete {
	private String id;
	private String name;
	private String win_identity;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWin_identity() {
		return win_identity;
	}
	public void setWin_identity(String winIdentity) {
		win_identity = winIdentity;
	}
	

}
