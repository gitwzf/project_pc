package com.wzf.model;

public class Week {
	private String year;
	private String month;
	private String weeknumber;
	private String upworks;
	private String simpledate;
	private String begdata;
	private String sbegdata;
	private String enddata;
	
	
	public String getSbegdata() {
		return sbegdata;
	}
	public void setSbegdata(String sbegdata) {
		this.sbegdata = sbegdata;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getWeeknumber() {
		return weeknumber;
	}
	public void setWeeknumber(String weeknumber) {
		this.weeknumber = weeknumber;
	}
	public String getSimpledate() {
		return simpledate;
	}
	public void setSimpledate(String simpledate) {
		this.simpledate = simpledate;
	}
	public String getUpworks() {
		return upworks;
	}
	public void setUpworks(String upworks) {
		this.upworks = upworks;
	}
	public String getBegdata() {
		return begdata;
	}
	public void setBegdata(String begdata) {
		this.begdata = begdata;
	}
	public String getEnddata() {
		return enddata;
	}
	public void setEnddata(String enddata) {
		this.enddata = enddata;
	}
	

}
