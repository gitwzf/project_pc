package com.wzf.model;

public class Voteuser {
	private String id;
	private String username;
	private String voteuserkey;
	private String votenumbers;
	private String votedetailnumbers;
	private String votepaintnumbers;
	
	
	public String getVotepaintnumbers() {
		return votepaintnumbers;
	}
	public void setVotepaintnumbers(String votepaintnumbers) {
		this.votepaintnumbers = votepaintnumbers;
	}
	public String getVotedetailnumbers() {
		return votedetailnumbers;
	}
	public void setVotedetailnumbers(String votedetailnumbers) {
		this.votedetailnumbers = votedetailnumbers;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getVoteuserkey() {
		return voteuserkey;
	}
	public void setVoteuserkey(String voteuserkey) {
		this.voteuserkey = voteuserkey;
	}
	public String getVotenumbers() {
		return votenumbers;
	}
	public void setVotenumbers(String votenumbers) {
		this.votenumbers = votenumbers;
	}
	

}
